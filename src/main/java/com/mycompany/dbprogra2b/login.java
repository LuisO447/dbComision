    public boolean login(Usuario dato){
        
        PreparedStatement stmt = null;
        ResultSet rs=null;
        Connection conn = null;
        Usuario usuario = new Usuario();
        boolean tienePermiso = false;
        
        String sqlS ="select id_usuario,username,password from usuario where id_usuario=?";
        
        try{
            
            stmt=conn.prepareStatement(sqlS);
            stmt.setString(1, dato.getUsername());
            rs=stmt.executeQuery();
            
            if(rs.next()){
                
                if(dato.getPassword().equals(rs.getString(3))){
                
                    dato.setId_usuario(rs.getInt(1));
                    dato.setUsername(rs.getString(2));
                    return true;
                }else{
                    return false;
                
                }
                
            }
            
            return false;
            
        } catch (SQLException ex){
            ex.printStackTrace(System.out);
            return false;
        }
            
        
    }
    
